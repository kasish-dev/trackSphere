// Get attendance statistics
exports.getAttendanceStats = async (req, res) => {
  try {
    const { workspaceId } = req.params;
    const { startDate, endDate } = req.query;

    // Check access
    const user = await User.findById(req.user.id).populate('workspace', 'name');
    if (!(await canAccessWorkspace({ actor: user, workspaceId }))) {
      return res.status(403).json({ message: 'Access denied' });
    }

    const start = startDate ? new Date(startDate) : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000); // 30 days ago
    const end = endDate ? new Date(endDate) : new Date();

    const attendanceRecords = await Attendance.find({
      workspace: workspaceId,
      date: { $gte: start, $lte: end },
    }).populate('user', 'name email');

    // Calculate statistics
    const stats = {
      totalRecords: attendanceRecords.length,
      present: attendanceRecords.filter(r => r.status === 'present').length,
      late: attendanceRecords.filter(r => r.status === 'late').length,
      absent: attendanceRecords.filter(r => r.status === 'absent').length,
      halfDay: attendanceRecords.filter(r => r.status === 'half-day').length,
      averageHours: 0,
      totalLateMinutes: 0,
    };

    const recordsWithHours = attendanceRecords.filter(r => r.totalHours > 0);
    if (recordsWithHours.length > 0) {
      stats.averageHours = recordsWithHours.reduce((sum, r) => sum + r.totalHours, 0) / recordsWithHours.length;
    }

    stats.totalLateMinutes = attendanceRecords.reduce((sum, r) => sum + (r.lateMinutes || 0), 0);

    res.json(stats);
  } catch (error) {
    console.error('Error fetching attendance stats:', error);
    res.status(500).json({ message: 'Server error' });
  }
};
